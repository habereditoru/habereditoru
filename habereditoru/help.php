							<div class="help-tab-content active" id="tab-panel-overview" style="">
								<p>Kategorileri kullanarak sitenizi bölümlendirebilir ve ilişkili yazıları gruplayabilirsiniz. <a href="options-writing.php">Yazma ayarları</a>ndan değiştirene kadar varsayılan kategori “Genel” kategorisidir.</p><p>Kategoriler ve etiketler arasındaki fark nedir? Normalde etiketler yazınız hakkında önemli bilgiler içeren, diğer yazılarda tekrarlama ihtimali olan anahtar kelimelerdir (isimler, konular vb.). Bunun yanında kategoriler önceden belirlenmiş bölümlerdir. Eğer sitenizi bir kitap gibi düşünürseniz, kategoriler kitabın içindekiler bölümü gibi, etiketler ise kitabın indeks bölümü gibidir.</p>							</div>
						
							<div class="help-tab-content" id="tab-panel-adding-terms" style="display: none;">
								<p>Yeni bir kategori eklerken şu alanları doldurmalısınız:</p><ul><li><strong>İsim</strong> &mdash; İsmin sitenizde nasıl gözükeceğidir.</li><li><strong>Kısa isim</strong> &mdash; “Kısa isim” isimlerin web adresleriyle uyumlu halleridir. Genellikle tamamı küçük harflerden oluşur ve sadece harfler, rakamlar ve noktalama işaretleri içerir.</li><li><strong>Üst</strong> &mdash; Kategoriler, etiketlerin tersine bir hiyerarşiye sahiptirler. Jazz isimli bir kategorinin altında Bebop ve Big Band isimli alt kategorileriniz olabilir. Tamamen seçime bağlı. Bir alt kategori oluşturmak için Üst kategori kutusundan başka bir kategori seçin.</li><li><strong>Açıklama</strong> &mdash; Açıklama varsayılan olarak gerekli değildir, yine de bazı temalar açıklamayı görüntüleyebilir.</li></ul><p>Ekran seçenekleri sekmesinden kaç adet eleman gözükeceği ve tablodaki hangi sütunların gösterilip gizleneceği gibi ayarlar yaparak bu ekranın görünümünü değiştirebilirsiniz.</p>							</div>
